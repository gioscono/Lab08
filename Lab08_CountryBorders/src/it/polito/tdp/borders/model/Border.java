package it.polito.tdp.borders.model;

public class Border {
	
	private int c1Code;
	private int c2Code;
	
	public Border(int c1Code, int c2Code) {
		this.c1Code = c1Code;
		this.c2Code = c2Code;
	}

	public int getC1Code() {
		return c1Code;
	}

	public int getC2Code() {
		return c2Code;
	}
	
	
	
}
